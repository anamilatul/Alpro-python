def jumlah(bil1,bil2):
    hasil = bil1 + bil2
    print("Hasil dari ",bil1,"+",bil2,"=",hasil)

def kurang(bil1,bil2):
    hasil = bil1 - bil2
    print("Hasil dari ",bil1,"-",bil2,"=",hasil)

def kali(bil1,bil2):
    hasil = bil1 * bil2
    print("Hasil dari ",bil1,"x",bil2,"=",hasil)

def bagi(bil1,bil2):
    hasil = bil1 / bil2
    print("Hasil dari ",bil1,":",bil2,"=",hasil)